# Based on Coordinate return the location
#(2,3) = Bank
#(3,5) = Office
#(5,8) = Store
#Any other value unknown location


location = (5,8)

if location == (2,3):
    print ("Location is Bank")
elif location == (3,5):
    print("Location is Office")
elif location == (5,8):
    print("Location is Store")
else:
    print("Location is Unknown")
